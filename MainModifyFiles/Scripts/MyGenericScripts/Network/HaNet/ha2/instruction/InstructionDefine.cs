	GLOBAL_EVENT		 		= 0x64,
	GET_PROPERTY_REQ	 		= 0x70,
	GET_PROPERTY_RES	 		= 0x71,
	SET_PROPERTY_REQ	 		= 0x73,
	SET_PROPERTY_RES	 		= 0x74,
}
